// Define the function
function generateGreeting(name) {
    return `Hello, ${name}! Welcome to our site.`;
  }
  
  // Example: Greet three different people
  console.log(generateGreeting('Alice'));  // Output: Hello, Alice! Welcome to our site.
  console.log(generateGreeting('Bob'));    // Output: Hello, Bob! Welcome to our site.
  console.log(generateGreeting('Charlie')); // Output: Hello, Charlie! Welcome to our site.
  